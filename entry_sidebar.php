        <?php
        // Build archive counts by month (YYYY-MM) from both blog and works posts
        $archive_counts = [];
        $archive_limit = 100;
        foreach (['/blog', '/works'] as $archive_endpoint) {
            $archive_offset = 0;
            $archive_total = 0;

            do {
                $archive_response = microcms_get($archive_endpoint . "?limit=" . $archive_limit . "&offset=" . $archive_offset);
                if (!$archive_response || empty($archive_response->contents)) {
                    break;
                }

                foreach ($archive_response->contents as $archive_post) {
                    $archive_date = '';
                    if (!empty($archive_post->publishedAt)) {
                        $archive_date = $archive_post->publishedAt;
                    } elseif (!empty($archive_post->createdAt)) {
                        $archive_date = $archive_post->createdAt;
                    }

                    if ($archive_date === '') {
                        continue;
                    }

                    $archive_timestamp = strtotime($archive_date);
                    if ($archive_timestamp === false) {
                        continue;
                    }

                    $archive_key = date('Y-m', $archive_timestamp);
                    if (!isset($archive_counts[$archive_key])) {
                        $archive_counts[$archive_key] = 0;
                    }
                    $archive_counts[$archive_key]++;
                }

                $archive_offset += $archive_limit;
                $archive_total = isset($archive_response->totalCount) ? (int)$archive_response->totalCount : $archive_offset;
            } while ($archive_offset < $archive_total);
        }

        if (!empty($archive_counts)) {
            krsort($archive_counts);
        }
        ?>

        <?php if (!empty($archive_counts)): ?>
            <div class="blog_month">
                <ul>
